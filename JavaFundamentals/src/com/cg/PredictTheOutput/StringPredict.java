package com.cg.PredictTheOutput;

public class StringPredict {

	public static void main(String[] args) {
		System.out.print("Y"+"O");
		System.out.print('L'+'O');
		/*First statement -> YO
		  Second statement -> 155
		  As per the ASCII norms 
		  L equivalent in decimal is 76
		  O equivalent in decimal is 79
		  So 76 + 79 = 155
		  Thus answer is YO155.
		 */
	}

}
