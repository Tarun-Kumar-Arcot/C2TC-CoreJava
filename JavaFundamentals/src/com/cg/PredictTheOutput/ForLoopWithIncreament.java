package com.cg.PredictTheOutput;

public class ForLoopWithIncreament {

	public static void main(String[] args) {
		for(int i = 0; i < 5;i++) {
			System.out.println("Hello World!");
			i++;
		}
		
		/*
		 i = 0 prints Hello World! -> i =1
		 i = 2 prints Hello World! -> i =3
		 i = 4 prints Hello World! -> i =5
		 */
	}

}
